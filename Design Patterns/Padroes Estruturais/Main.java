/*
    Padroes Estruturais: relaciona-se a criação de grandes estruturas de classes. Há um alto uso de herança nesses padroes.
                         Sempre lembrando, que essas estruturas são construidas de modo que haja um fraco acoplamento entre os componentes.


        Padrao Decorator: é usado quando queremos criar uma classe basica de objetos. Esses objetos possuirao comportamentos extras opcionais 
                          independentes entre si (para serem usados em situaçoes especificas), contruidos de modo que eles nao interfiram na 
                          classe basica inicial.


        Padrao Adapter: é usado quando ocorrem mudanças em uma hierarquia/conjunto de classes (e.g, biblioteca) e 
                        o nosso codigo precisa se adaptar a essas mudanças com o menor impacto possivel.
*/